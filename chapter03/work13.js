// オブジェクトのスプレッド演算子2
const score1 = {
    '国語' : 88,
    '数学' : 96,
    '英語' : 92,
};

const score2 = {
    '社会' : 85,    
    '理科' : 89,
    "国語" : 95,
};

// オブジェクトの要素を展開して、結合する
// 同じキーがあれば、後方のオブジェクトのキーで上書きする
const score3 = { ...score1, ...score2 };
// { '国語': 95, '数学': 96, '英語': 92, '社会': 85, '理科': 89 }
console.log( score3 );
